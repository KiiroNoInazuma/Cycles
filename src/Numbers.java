public class Numbers {
    public static void main(String[] args) {
        int num = 0;
        while (num < 10) {
            num++;
            System.out.print(num + "\t");
        }
        System.out.println();
        for (num = 10; num > 0; num--) {
            System.out.print(num + "\t");
        }
    }
}
